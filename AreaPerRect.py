ssrec = float (input("How long is the shortest side of your rectangle in cm? "))
lsrec = float (input ("How long is the longest side of your rectangle in cm? "))
per = (ssrec * 2) + (lsrec * 2)
area = ssrec * lsrec
print ("the perimeter of the rectangle is", per, "cm")
print ("the area of the rectangle is", area, "cms squared")
